/**
*Clear Screen function blanks out the Playing Field in a color.
*init function.
*/
void Clear_Buffer(uint16_t newColor){
	color = newColor;
	for(uint32_t i = 0; i < 60*120; i++){
		PlayingFieldBuffer[i] = color;
	}
}

/**
*Set_Screen is a set method to set the Refresh function that's called on interrupt.
*/
void Set_Screen(uint8_t in){
	screen = in;
}

/**
*Refresh function redraws the screen.
*param - screen top or bottom screen (0 - bottom screen, 1 - top)
*/
void Refresh(){
	if(screen == 1)
		ST7735_DrawBitmap(5, 70, PlayingFieldBuffer, 120, 60);
	else
		ST7735_DrawBitmap(5, 160, PlayingFieldBuffer, 120, 60);
	GPIO_PORTF_DATA_R ^= 0x02;
}
/**
*Draw Sprite function takes a sprite and draws it onto the Playing Field.
*param - x of top left coord (assume all sprites are in boxes)
*param - y of top left coord
*param - sprite reference
*param - width of sprite
*param - height of sprite
*error handling to prefev
*/
void Draw_Sprite(int32_t x, int32_t y, const unsigned short sprite[], uint16_t width, uint16_t height){
	uint32_t index = 0, bufferIdx;
	for(int32_t row = y; row < (y + height); row++){
		for(int32_t column = x; column < (x + width); column ++){
			bufferIdx = row * 120 + column;
			//write pixel to screen if within bounds
			if(row >= 0 && row < 120 && column >= 0 && column < 120)
				PlayingFieldBuffer[bufferIdx] = sprite[index];
			index ++;
		}
	}
}

/**
*unDraw Sprite function takes a sprite box and redraws the bg
*param - x of top left coord (assume all sprites are in boxes)
*param - y of top left coord
*param - width of sprite
*param - height of sprite
*take note of what sprite is being undrawn
*/
void unDraw_Sprite(int32_t x, int32_t y, uint16_t width, uint16_t height){
	uint32_t index = 0, bufferIdx;
	for(int32_t row = y; row < (y + height); row++){
		for(int32_t column = x; column < (x + width); column ++){
			bufferIdx = row * 120 + column;
			//redraw bg
			//write pixel to screen if within bounds
			if(row >= 0 && row < 120 && column >= 0 && column < 120)
				PlayingFieldBuffer[bufferIdx] = color;
			index ++;
		}
	}
}


/**
* function jump moves sprite up 15 pixels and down.
* param: x - lower left pixel x coord of image
* param: y - lower left pixel y coord of image
* param: *sprite - reference to image sprite
* param: width - width of sprite
* param: height - height of sprite
* param: speed - speed of jump
*/
void jump(uint16_t x, uint16_t y, const unsigned short sprite[], uint16_t width, uint16_t height, uint8_t speed){
	uint8_t up, down;
	for(up = 0; up < 15; up += speed){
		Draw_Sprite(x, y + up, sprite, width, height);
		for(int i = 72724*3/2; i > 0; i--);
		unDraw_Sprite(x, y + up, width, height);
	}
	for(down = 0; down < 15; down += speed){ 
		Draw_Sprite(x, y + up - down, sprite, width, height);
		for(int i = 72724*3/2; i > 0; i--);
		unDraw_Sprite(x, y + up - down, width, height);
	}
}

