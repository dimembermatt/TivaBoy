/**
*Graphics_Init sets up Timer3 interrupt
*/
void Graphics_Init(void){
	Timer3_Init(&Refresh, 3333333);	//refresh rate of 26fps
}

/**
*Turns on and off buffer refresh interrupt
*/
void Start_Refresh(void){
	TIMER3_TAILR_R = 3333333;
	TIMER3_TAPR_R = 0;
	TIMER3_CTL_R = 0x00000001;
}
void Stop_Refresh(void){
	TIMER3_CTL_R = 0x00000000;
}

